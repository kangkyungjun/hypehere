def main():
    # File path
    countries_file = os.path.join(
        os.path.dirname(os.path.dirname(__file__)),
        'static', 'js', 'countries.js'
    )

    print(f"Reading {countries_file}...")

    with open(countries_file, 'r', encoding='utf-8') as f:
        content = f.read()

    # Parse each country entry
    pattern = r'\{\s*code:\s*"([^"]*)",\s*name:\s*"([^"]*)",\s*nameEn:\s*"([^"]*)"\s*\}'

    def replace_country(match):
        code = match.group(1)
        name_ko = match.group(2)
        name_en = match.group(3)

        # Get translations
        translation = COUNTRY_TRANSLATIONS.get(code, {})
        name_ja = translation.get('ja', name_en)  # Fallback to English
        name_es = translation.get('es', name_en)  # Fallback to English

        # Build new entry with proper formatting and indentation
        return f'{{ code: "{code}", name: "{name_ko}", nameEn: "{name_en}", nameJa: "{name_ja}", nameEs: "{name_es}" }}'

    # Replace all country entries
    new_content = re.sub(pattern, replace_country, content)

    # Write back to file
    print(f"Writing updated content to {countries_file}...")
    with open(countries_file, 'w', encoding='utf-8') as f:
        f.write(new_content)

    print("✓ Successfully added Japanese and Spanish translations to all countries!")
    print(f"✓ Total countries updated: {len(re.findall(pattern, content))}")


if __name__ == '__main__':
    main()
