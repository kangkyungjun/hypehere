def add_spanish_translations(po_file_path):
    """Add Spanish translations to django.po file"""
    import re

    with open(po_file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Counter
    translated_count = 0

    # For each translation in our mapping
    for korean, spanish in TRANSLATIONS.items():
        # Escape special regex characters in korean text
        escaped_korean = re.escape(korean)

        # Pattern to find msgid with empty msgstr
        # Look for exact match of msgid followed by empty msgstr
        pattern = rf'(msgid\s+"{escaped_korean}"\s*\n)(msgstr\s+"")'

        # Replacement with Spanish translation
        replacement = rf'\1msgstr "{spanish}"'

        # Count matches before replacement
        matches = len(re.findall(pattern, content))
        if matches > 0:
            content = re.sub(pattern, replacement, content)
            translated_count += matches
            print(f"âœ“ Translated: {korean[:50]}... â†’ {spanish[:50]}... ({matches} occurrence(s))")

    # Write back
    with open(po_file_path, 'w', encoding='utf-8') as f:
        f.write(content)

    return translated_count


